import config from "@config/config.json";
import theme from "@config/theme.json";
import { Analytics } from '@vercel/analytics/react';
import { JsonContext } from "context/state";
import Head from "next/head";
import { useEffect, useState } from "react";
import TagManager from "react-gtm-module";
import "styles/style.scss";
import { useRouter } from "next/router";
import ReactGA from "react-ga";

const App = ({ Component, pageProps }) => {
  // import google font css
  const pf = theme.fonts.font_family.primary;
  const sf = theme.fonts.font_family.secondary;
  const router = useRouter();
  const canonicalUrl = (`https://placea.in` + (router.asPath === "/" ? "": router.asPath)).split("?")[0];
  const [fontcss, setFontcss] = useState();
  useEffect(() => {
    fetch(
      `https://fonts.googleapis.com/css2?family=${pf}${
        sf ? "&family=" + sf : ""
      }&display=swap`
    ).then((res) => res.text().then((css) => setFontcss(css)));
  }, [pf, sf]);
  useEffect(() => {
    ReactGA.initialize("G-RWW57SD920"); // Replace with your GA4 Measurement ID
    ReactGA.pageview(window.location.pathname + window.location.search);
  }, [useRouter().asPath]);

  // google tag manager (gtm)
  const tagManagerArgs = {
    gtmId: config.params.tag_manager_id,
  };
  useEffect(() => {
    setTimeout(() => {
      config.params.tag_manager_id && TagManager.initialize(tagManagerArgs);
    }, 5000);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);

  return (
    <JsonContext>
      <Head>
      {canonicalUrl && <link rel="canonical" href={canonicalUrl} />}
        {/* google font css */}
        <link
          rel="preconnect"
          href="https://fonts.gstatic.com"
          crossOrigin="true"
        />
        <style
          dangerouslySetInnerHTML={{
            __html: `${fontcss}`,
          }}
        />
        {/* responsive meta */}
        <meta
          name="viewport"
          content="width=device-width, initial-scale=1, maximum-scale=5"
        />
      </Head>
      <Component {...pageProps} />
    </JsonContext>
  );
};

export default App;
